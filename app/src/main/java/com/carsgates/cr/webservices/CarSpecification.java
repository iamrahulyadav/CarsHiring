public class CarSpecification {

    public String specification_name ;
    public String specification_description ;
    public String specification_display ;
    public String specification_id ;

    public String getSpecification_id() {
        return specification_id;
    }

    public void setSpecification_id(String specification_id) {
        this.specification_id = specification_id;
    }

    public String getSpecification_name() {
        return specification_name;
    }

    public void setSpecification_name(String specification_name) {
        this.specification_name = specification_name;
    }

    public String getSpecification_description() {
        return specification_description;
    }

    public void setSpecification_description(String specification_description) {
        this.specification_description = specification_description;
    }

    public String getSpecification_display() {
        return specification_display;
    }

    public void setSpecification_display(String specification_display) {
        this.specification_display = specification_display;
    }


}
